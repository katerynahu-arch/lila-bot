    """–î–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–ø–∏—Å—å –≤ –∏—Å—Ç–æ—Ä–∏—é —Ö–æ–¥–æ–≤"""
    user = get_user(user_id)
    if user:
        new_history = user['history'] + entry + '\n'
