def get_user(user_id):
    conn = sqlite3.connect(DB_NAME)
    cur = conn.cursor()
    cur.execute('SELECT * FROM users WHERE user_id = ?', (user_id,))
    row = cur.fetchone()
    conn.close()
    if row:
        return {
            'user_id': row[0],
            'position': row[1],
            'entered': bool(row[2]),
            'game_active': bool(row[3]),
            'pending_sixes': row[4],
            'waiting_for_roll': bool(row[5]),
            'query': row[6] or '',
            'history': row[7] or ''
        }
    return None

def save_user(data):
    conn = sqlite3.connect(DB_NAME)
    cur = conn.cursor()
    cur.execute('''
        INSERT OR REPLACE INTO users (user_id, position, entered, game_active, pending_sixes, waiting_for_roll, query, history)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?)
    ''', (
        data['user_id'],
        data['position'],
        int(data['entered']),
        int(data['game_active']),
        data['pending_sixes'],
        int(data['waiting_for_roll']),
        data['query'],
        data['history']
    ))
    conn.commit()
    conn.close()

